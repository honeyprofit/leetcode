from random import choice
import art

def add(n1, n2):
    return n1 + n2

def subtract(n1, n2):
    return n1 - n2

def multiply(n1, n2):
    return n1 * n2

def divide(n1, n2):
    return n1 / n2

operations = {
    "+": add,
    "-": subtract,
    "*": multiply,
    "/": divide,
}

# print(operations["*"](4,8))

# print(logo)
def calculator():
    should_accumulate = True
    print(art.logo)
    num1 = float(input("What is the first number?"))

    while(should_accumulate):
        for symbol in operations:
            print(symbol)
        operation = input ("Pick an operation: ")
        num2 = float(input("What is the next number?"))
        answer = operations[operation](num1, num2)
        print(f"{num1} {operation} {num2} = {answer}")

        choice = input(f"Type 'y' to continue calcurating with {answer}, or type 'n' to start a new: ")
        if(choice) == "y":
            num1 = answer
        else:
            should_accumulate = False
            print("\n"*20)
            calculator()

calculator()
